package DasarPemrograman;

public class Operator_Relational {
    
    public static void main(String[] args){
       
        System.out.println("---Operasi aritmetika pada tipe integer---");
        
        //Deklarasi 
        int a = 5;
        int b = 10;
        
        //Output
        System.out.println("a == b bernilai " + (a == b));
        System.out.println("a != b bernilai " + (a != b));
        System.out.println("a > b bernilai " + (a > b));
        System.out.println("a < b bernilai " + (a < b));
        System.out.println("a >= b bernilai " + (a >= b));
        System.out.println("a <= b bernilai " + (a <= b));
     
    }
}
